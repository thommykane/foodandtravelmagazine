import { NextRequest, NextResponse } from "next/server";
import { db } from "@/lib/db";
import { announcements } from "@/lib/db/schema";
import { eq, desc } from "drizzle-orm";
import { requireAdmin } from "@/lib/admin";
import { v4 as uuid } from "uuid";

async function checkAdmin() {
  try {
    await requireAdmin();
    return true;
  } catch {
    return false;
  }
}

export async function GET() {
  if (!(await checkAdmin())) {
    return NextResponse.json({ error: "Forbidden", announcements: [] }, { status: 403 });
  }
  const all = await db.select().from(announcements).orderBy(desc(announcements.createdAt));
  return NextResponse.json({ announcements: all });
}

export async function POST(req: NextRequest) {
  try {
    const { user } = await requireAdmin();

    const body = await req.json().catch(() => ({}));
    const { title, body: bodyText } = body;

    if (!title?.trim() || !bodyText?.trim()) {
      return NextResponse.json({ error: "title and body required" }, { status: 400 });
    }

    const id = uuid();
    await db.insert(announcements).values({
      id,
      title: title.trim(),
      body: bodyText.trim(),
      createdById: user.id,
      active: true,
    });

    return NextResponse.json({ success: true, id });
  } catch {
    return NextResponse.json({ error: "Forbidden" }, { status: 403 });
  }
}

export async function DELETE(req: NextRequest) {
  if (!(await checkAdmin())) {
    return NextResponse.json({ error: "Forbidden" }, { status: 403 });
  }

  const { searchParams } = new URL(req.url);
  const id = searchParams.get("id");

  if (!id) {
    return NextResponse.json({ error: "id required" }, { status: 400 });
  }

  await db.delete(announcements).where(eq(announcements.id, id));
  return NextResponse.json({ success: true });
}
